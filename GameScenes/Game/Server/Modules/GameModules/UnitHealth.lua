local module = {}

function module:CreateUnitHealth(unit)
	local gui = game.ReplicatedStorage.TowerDefenseLocalPackages.Guis.EnemyDispaly:Clone()
	gui.EnemyName.Text = unit.Name
	unit.Humanoid.MaxHealth = units[unit.Name].MaxHealth + (units[unit.Name].MaxHealth * .2) * (#game.Players:GetChildren()-1)
	unit.Humanoid.Health = units[unit.Name].Health +  (units[unit.Name].Health * .2) * (#game.Players:GetChildren()-1)
	gui.TextLabel.Text = units[unit.Name].Health + (units[unit.Name].Health * .2) * (#game.Players:GetChildren()-1)
	unit:SetAttribute("Shield",units[unit.Name].Shield)
	unit:SetAttribute("Targetable",true)
	unit.Humanoid.Health = units[unit.Name].Health +  (units[unit.Name].Health * .2) * (#game.Players:GetChildren()-1)
	if units[unit.Name].Shield >= 0 then
		gui.Shield.TextLabel.Text = units[unit.Name].Shield
	else
		gui.Shield.Visible = false
	end
	gui.Name = "EnemyDisplay"
	gui.Parent = unit.Head
	
end

return module
